// @lc code=start
/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} s
 * @param {TreeNode} t
 * @return {boolean}
 */
var isSubtree = function (s, t) {
  /**
   * @return {boolean}
   */
  const compare = (root1, root2) => {
    if (root1 === null && root2 === null) return true
    if (root1 === null || root2 === null) return false

    return root1.val === root2.val && compare(root1.left, root2.left) && compare(root1.right, root2.right)
  }

  const dfs = [s]
  while (dfs.length > 0) {
    const node = dfs.pop()

    if (node.val === t.val && compare(node, t)) return true

    if (node.right) dfs.push(node.right)
    if (node.left) dfs.push(node.left)
  }
}
// @lc code=end
module.exports = isSubtree
